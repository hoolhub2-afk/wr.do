const linuxDoProvider: any = {
  id: "linuxdo",
  name: "Linux Do",
  type: "oauth",
  authorization: {
    url: "https://connect.linux.do/oauth2/authorize",
    params: { scope: "user" },
  },
  token: "https://connect.linux.do/oauth2/token",
  userinfo: "https://connect.linux.do/api/user",
  clientId: env.LinuxDo_CLIENT_ID,
  clientSecret: env.LinuxDo_CLIENT_SECRET,
  checks: ["state"],
  client: {
    token_endpoint_auth_method: "client_secret_post",
  },
  profile: (profile: any) => {
    console.log("profile", profile);
    // Use unavatar.io to get avatar, fallback to dicebear
    // unavatar supports GitHub, Twitter, etc. For linux.do, use dicebear with username
    const avatarUrl = `https://api.dicebear.com/9.x/initials/svg?seed=${encodeURIComponent(profile.username || profile.name || 'user')}`;

    return {
      id: profile.id.toString(),
      name: profile.username,
      image: avatarUrl,
      email: profile.email,
      active: profile.active ? 1 : 0,
    };
  },
};

